package main

import "fmt"

func main() {
	var cadena string = "Inicial"
	fmt.Println(cadena)
	cadena = "Modificada"
	fmt.Println(cadena)

	var entero1, entero2 int = 10, 20
	fmt.Println("Enteros:", entero1, entero2)

	var booleano = true
	fmt.Println(booleano)

	var enteroSimple int
	fmt.Println(enteroSimple)
	enteroSimple = 42
	fmt.Println(enteroSimple)

	fruta := "Manzana"
	fmt.Println(fruta)
}
